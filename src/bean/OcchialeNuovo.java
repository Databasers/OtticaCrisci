package bean;

import java.util.Date;

public class OcchialeNuovo {
	
	int idOcchiale,prezzo;
	String stato,cF;
	Date dataOrdine,dataRitiro;
	
	public int getId() {
		return idOcchiale;
	}
	public void setId(int id) {
		this.idOcchiale = id;
	}
	public int getPrezzo() {
		return prezzo;
	}
	public void setPrezzo(int prezzo) {
		this.prezzo = prezzo;
	}
	public String getStato() {
		return stato;
	}
	public void setStato(String stato) {
		this.stato = stato;
	}
	public Date getDataOrdine() {
		return dataOrdine;
	}
	public void setDataOrdine(Date dataOrdine) {
		this.dataOrdine = dataOrdine;
	}
	public Date getDataRitiro() {
		return dataRitiro;
	}
	public void setDataRitiro(Date dataRitiro) {
		this.dataRitiro = dataRitiro;
	}
	public int getIdOcchiale() {
		return idOcchiale;
	}
	public void setIdOcchiale(int idOcchiale) {
		this.idOcchiale = idOcchiale;
	}
	public String getcF() {
		return cF;
	}
	public void setcF(String cF) {
		this.cF = cF;
	}
	
	
	

}
